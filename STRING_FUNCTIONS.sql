SELECT RIGHT('PIXEL', 4)

-- LEN: UZUNLUK
SELECT LEN('YUNUS')

-- ----------------------------

-- TRIM, LTRIM, RTRIM: BOÞLUKLARI TEMÝZLER
SELECT TRIM('     CEREN  ')		-- CEREN
SELECT LTRIM('     CEREN  ')	-- CEREN    --
SELECT RTRIM('     CEREN  ')	--     CEREN

-- ---------------------------

-- LOWER, UPPER: BÜYÜK KÜÇÜK HARF
