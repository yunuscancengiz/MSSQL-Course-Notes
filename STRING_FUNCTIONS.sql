-- CHARINDEX: STRING ÝÇÝNDE BÝR STRING'I BULUR VE INDEX'INI SÖYLER
SELECT CHARINDEX('N', 'YUNUS', 1)

-- -----------------------------

-- CONCAT: 2 VEYA DAHA FAZLA STRING'Ý YAN YANA BÝRLEÞTÝRÝR
SELECT CONCAT('YUNUS', 'CEREN')
SELECT 'MATHILDA' + 'PIXEL'

-- CONCATWS: SEPERATOR GÖNDERÝLÝR
SELECT CONCAT_WS(' ~ - ~ ', 'YUNUS', 'CAN', 'CENGÝZ')

-- TABLODA KULLANIMLARI
SELECT CONCAT_WS(' | ** | ', USERNAME_, PASSWORD_) FROM USER_

-- -----------------------------

-- FORMAT: SAYI YA DA TARÝH TÜRÜNDEKÝ DEÐERLERÝ ÝSTEDÝÐÝMÝZ FORMATTA YAZDIRMAMIZI SAÐLAR
SELECT GETDATE()	-- BUGÜNÜN TARÝHÝNÝ GETÝRÝR
SELECT FORMAT(GETDATE(), 'd', 'EN-US')	-- RESULT: 5/25/2025
SELECT FORMAT(GETDATE(), 'D', 'EN-US')	-- RESULT: Sunday, May 25, 2025


-- -------------------------------

-- LEFT, RIGHT: SOL VEYA SAÐDAN ÝSTENÝLEN SAYIDA KARAKTER ALMAYI SAÐLAR
