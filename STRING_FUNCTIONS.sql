-- ASCII VE CHAR
-- ASCII ÝÇÝNE ALDIÐI STRING'ÝN ASCII KARÞILIÐINI VERÝR
SELECT ASCII('C')  -- RESULT: 67

-- CHAR ÝÇÝNE ALDIÐI ASCII DEÐERÝNÝN SEMBOL KARÞILIÐINI VERÝR
SELECT CHAR('67')  -- RESULT: C

-- FONKSÝYONLAR ÝÇ ÝÇE KULLANILABÝLÝR
SELECT ASCII(CHAR('67'))  -- RESULT: 67


